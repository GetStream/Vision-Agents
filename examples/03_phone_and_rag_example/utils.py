def mulaw_to_pcm(mulaw_bytes: bytes) -> PcmData:
    """Convert mulaw audio bytes to PcmData using lookup table."""
    mulaw_samples = np.frombuffer(mulaw_bytes, dtype=np.uint8)
    samples = MULAW_DECODE_TABLE[mulaw_samples]

    return PcmData(
        samples=samples,
        sample_rate=TWILIO_SAMPLE_RATE,
        channels=1,
        format=AudioFormat.S16,
    )


def pcm_to_mulaw(pcm: PcmData) -> bytes:
    """Convert PcmData to mulaw bytes for Twilio."""
    # Resample to 8kHz mono if needed
    if pcm.sample_rate != TWILIO_SAMPLE_RATE or pcm.channels != 1:
        pcm = pcm.resample(target_sample_rate=TWILIO_SAMPLE_RATE, target_channels=1)

    samples = pcm.samples.astype(np.int16)

    # Encode to mulaw (ITU-T G.711)
    MULAW_BIAS = 33
    MULAW_MAX = 32635

    sign = np.where(samples < 0, 0x80, 0)
    samples = np.abs(samples).clip(max=MULAW_MAX)
    samples = samples + MULAW_BIAS

    # Find exponent (segment)
    exponent = np.floor(np.log2(samples)).astype(np.int32) - 5
    exponent = exponent.clip(min=0, max=7)

    # Extract mantissa
    mantissa = (samples >> (exponent + 3)) & 0x0F

    # Combine and invert
    mulaw = ~(sign | (exponent << 4) | mantissa) & 0xFF

    return mulaw.astype(np.uint8).tobytes()
